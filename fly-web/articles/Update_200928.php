<h3>Website flight map</h3>

<p>
	The flight map on the website has received a deserved update. There are many
	new additions and improvements.
<figure>
	<img class=fullsize src=/s/articles/upd200928fm.gif alt="new flight map" title="new flight map">
	<figcaption>Animated image of the new flight map</figcaption>
</figure>
<ul>
	<li>It's now zoomed in on the flight path
	<li>The flight path will never go outside the view again
	<li>Islands are drawn on the map
	<li>Runways are drawn on the map
	<li>Graphs received axes and are more clean
	<li>Graphs can be hovered, showing the value at the hovered point
	<li>On hovering, a plane icon shows on the route at the associated position
	<li>An attitude indicator has been added, showing the pitch and roll
</ul>
<p>
	<strong>Please note that the attitude indicator will only work for flights
	after August 9th 2020. The flights before that date did not save pitch/roll
	data yet.</strong>
<p>
	Please note that flights between August 9th 2020 and August 12th 2020 do not
	have altitude data due to a bug.
