<p>
	Every player has a score, which can be seen in-game on the scoreboard when
	pressing the Tab key, or at a user's profile page on the website. Score can be
	gained only by spending time playing on the server.
	The calculation is as follows:

<p>
	Sum of:
<ul>
	<li>1 point for every minute of flying time (either on mission or not)
	<li>1 point for every five minutes of non-flying, non-afk time
</ul>
