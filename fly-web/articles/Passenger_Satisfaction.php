<p>
	Passenger satisfaction is how satisified the passengers are when on a mission
	that involves passengers. It is influenced by the pitch and roll of the
	aircraft. When banking too much to either side, the satisfaction will decrease.
	When the pitch is very high or very low, the satisfaction will decrease.
<p>
	Having a passenger satisfaction less than 100% will negatively affect the pay
	of the mission. $30 is subtracted for every percent under 100, making the
	maximum penalty $3000.

<h3>HUD Indicator</h3>

<p>
	The passenger satisfaction is shown on the <?php link_article('HUD'); ?> when
	doing a mission that involves passengers. It is located right below the radar
	at the bottom left side of the screen.
