<h3 id=Passenger_cargo_planes>Passenger/Cargo planes</h3>

<p>
	These planes are available for use by the general public and
	are used to transport either passengers or cargo.

<h4 id=Andromada>Andromada</h4>

<figure style=float:right>
	<img src=/s/articles/andromada.png alt=Andromada title=Andromada>
	<figcaption>Andromada</figcaption>
</figure>
<p>
	Large cargo plane with four engines. The Andromada has a tailgate which can be opened,
	but sadly it's not possible to actually place anything inside its cargo hold.
<p>
	<strong>Based on:</strong> Lockheed C-5 Galaxy<br>
	<strong>Fuel capacity:</strong> <?=$SETTING__FUEL_CAP_ANDROM?>L<br>
	<strong>Fuel usage:</strong> <?=$SETTING__FUEL_USE_ANDROM?>L/s<br>

<h4 id=AT-400 class=clear>AT-400</h4>

<figure style=float:right>
	<img src=/s/articles/at-400.png alt=AT-400 title=AT-400>
	<figcaption>AT-400</figcaption>
</figure>
<p>
	Large passenger plane with two engines. The AT-400 is very maneuverable in the air
	but is unhandy to taxi with. Getting in might be a challenge, as it requires stairs.
	Fortunately, players can use the <code>/at400</code> command to get warped into
	the plane when the player is nearby.
<p>
	<strong>Based on:</strong> Being 737-500<br>
	<strong>Fuel capacity:</strong> <?=$SETTING__FUEL_CAP_AT400?>L<br>
	<strong>Fuel usage:</strong> <?=$SETTING__FUEL_USE_AT400?>L/s

<h4 id=Beagle class=clear>Beagle</h4>

<figure style=float:right>
	<img src=/s/articles/beagle.png alt=Beagle title=Beagle>
	<figcaption>Beagle</figcaption>
</figure>
<p>
	Twin-engine propellor passenger plane, for medium distances. Has a fixed landing gear
	and can perform landings and takeoffs on grass or dirt, but this requires some skill.
<p>
	<strong>Based on:</strong> Britten-Norman Islander<br>
	<strong>Fuel capacity:</strong> <?=$SETTING__FUEL_CAP_BEAGLE?>L<br>
	<strong>Fuel usage:</strong> <?=$SETTING__FUEL_USE_BEAGLE?>L/s<br>
	<strong>Trivia:</strong> A <a href=https://en.wikipedia.org/wiki/Beagle rel=nofollow>beagle</a><img src=/s/moin-www.png alt="globe icon" title="external link"> is a breed of small hound.

<h4 id=Dodo class=clear>Dodo</h4>

<figure style=float:right>
	<img src=/s/articles/dodo.png alt=Dodo title=Dodo>
	<figcaption>Dodo</figcaption>
</figure>
<p>
	Small and light short-range sportsplane with a single engine. The Dodo is very
	maneuverable and can take-off and land on a very short runway, which can even
	be grass or dirt.
<p>
	<strong>Based on:</strong> Cessna 152<br>
	<strong>Fuel capacity:</strong> <?=$SETTING__FUEL_CAP_DODO?>L<br>
	<strong>Fuel usage:</strong> <?=$SETTING__FUEL_USE_DODO?>L/s<br>
	<strong>See also:</strong> <a href=#Skimmer>Skimmer</a><br>
	<strong>Trivia:</strong> A <a href=https://en.wikipedia.org/wiki/Dodo rel=nofollow>Dodo</a><img src=/s/moin-www.png alt="globe icon" title="external link"> is an extinct bird that cannot fly.

