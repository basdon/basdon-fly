<h3 id=Other_planes class=clear>Other planes</h3>

<p>
	Apart from passenger and cargo planes, there are two other civilian planes that have a specific purpose.

<h4 id=Cropduster>Crop duster</h4>

<figure style=float:right>
	<img src=/s/articles/cropduster.png alt="Crop duster" title="Crop duster">
	<figcaption>Crop duster</figcaption>
</figure>
<p>
	Very small plane meant to spray crops. It's very easy to control and doesn't
	require a paved runway to takeoff or land.
<p>
	<strong>Based on:</strong> Grumman G-164 Ag-Cat<br>
	<strong>Fuel capacity:</strong> <?=$SETTING__FUEL_CAP_CROPDUST?>L<br>
	<strong>Fuel usage:</strong> <?=$SETTING__FUEL_USE_CROPDUST?>L/s<br>
	<strong>Trivia:</strong> Players can press the submission key (default:
	<code>2</code> or <code>numpad&nbsp;+</code>) to enable the nozzles,
	a white-colored smoke trail will be visible.

