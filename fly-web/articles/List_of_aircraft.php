<figure style=float:right>
	<img src=/s/articles/leviathan.png alt=Leviathan title=Leviathan>
	<figcaption>Leviathan</figcaption>
</figure>
<p>
	Heavy but stable helicopter. Has floats to make it possible to land on water.
	Even though it can land on water, it's hard or even impossible to get in
	as a swimming player.
